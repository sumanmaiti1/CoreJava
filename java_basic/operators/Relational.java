package corejava.java_basic.operators;

public class Relational {
	public static void main(String[] args) {
		System.out.println(2>5);
		System.out.println(2<5);
		System.out.println(4>=5);
		System.out.println(2<=5);
		System.out.println(3>3);
		System.out.println(3<3);		
		System.out.println(3>=3);
		System.out.println("Jay Shree Krishna"=="Jay Shree Krishna");
		System.out.println('a' != 'b'-1);
		System.out.println(true != false);
		System.out.println("Jay Shree Krishna" instanceof java.lang.String);
		System.out.println(new int[]{1,2} instanceof Object);
	}
}
